    def clear_terminal(self):
        """Clear the terminal screen."""
        os.system('cls' if os.name == 'nt' else 'clear')

    def show_frame(self):
        """Show the current frame of the animation."""
        self.clear_terminal()
        print(self.frames[self.current_frame])
        self.current_frame = (self.current_frame + 1) % len(self.frames)

    def animate(self, duration: float = None):
        """
        Animate the loading spiral.
        
        Args:
            duration (float, optional): How long to run the animation in seconds.
                                      If None, runs until interrupted.
        """
        start_time = time.time()
        try:
            while True:
                if duration and (time.time() - start_time) > duration:
                    break
                self.show_frame()
                time.sleep(0.2)
        except KeyboardInterrupt:
            self.clear_terminal() 